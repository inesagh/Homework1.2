package perimeterandarea;

public class Triangle extends CalculateAreaOrPerim {
    private int sideA;
    private int sideB;
    private int sideC;

    public Triangle(int sideA, int sideB, int sideC) {
        this.sideA = sideA;
        this.sideB = sideB;
        this.sideC = sideC;
    }

    public int getSideA() {
        return sideA;
    }

    public void setSideA(int sideA) {
        this.sideA = sideA;
    }

    public int getSideB() {
        return sideB;
    }

    public void setSideB(int sideB) {
        this.sideB = sideB;
    }

    public int getSideC() {
        return sideC;
    }

    public void setSideC(int sideC) {
        this.sideC = sideC;
    }

    @Override
    public void calculateArea() {
        double p = (double)(sideA + sideB + sideC) / 2;
        double area = Math.sqrt(p * (p - sideA) * (p - sideB) * (p - sideC));
        System.out.println("Area of triangle equals " + area);
    }

    @Override
    public void calculatePerimeter() {
        System.out.println("Perimeter of triangle equals " + (sideA + sideB + sideC));
        System.out.println("");
    }
}